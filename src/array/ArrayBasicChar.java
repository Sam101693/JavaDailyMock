package array;

public class ArrayBasicChar
{

	public static void main(String[] args) 
	{
		
		char a[] = {'b','a','c','d'};
		
		for(char b:a)
		{
			
			System.out.println(b);
		
		}

		
		for(int i=0;i<a.length;i++)
		{
			System.out.println(a[i]);
			
		}//for
		
			
	}//main
	
	
}//class
